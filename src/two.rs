#[derive(Debug, PartialEq)]
pub enum Game {
    Win,
    Lose,
    Draw,
}

impl Game {
    pub fn points(&self) -> usize {
        match self {
            Game::Win => 6,
            Game::Lose => 0,
            Game::Draw => 3,
        }
    }
}

pub const SAMPLE: &str = "A Y
B X
C Z";

pub fn play(opponent: char, you: char) -> Game {
    match (opponent, you) {
        ('A', 'X') => Game::Draw,
        ('A', 'Y') => Game::Win,
        ('A', 'Z') => Game::Lose,

        ('B', 'X') => Game::Lose,
        ('B', 'Y') => Game::Draw,
        ('B', 'Z') => Game::Win,

        ('C', 'X') => Game::Win,
        ('C', 'Y') => Game::Lose,
        ('C', 'Z') => Game::Draw,
        _ => panic!("Invalid chars!"),
    }
}

pub fn get_points(x: char) -> usize {
    match x {
        'X' => 1,
        'Y' => 2,
        'Z' => 3,
        _ => panic!("Invalid char in get_points!"),
    }
}

pub fn solve_first(input: &str) -> usize {
    let mut sum = 0;
    for line in input.lines() {
        let mut cs = line.chars();
        println!("{:?}", cs);
        let fst = cs.nth(0).unwrap();
        let snd = cs.nth(1).unwrap();
        sum += play(fst, snd).points() + get_points(snd)
    }
    sum
}

pub fn solve_second(input: &str) -> usize {
    let mut sum = 0;
    for line in input.lines() {
        let mut cs = line.chars();
        println!("{:?}", cs);
        let fst = cs.nth(0).unwrap();
        let snd = cs.nth(1).unwrap();
        let res = match snd {
            'X' => Game::Lose,
            'Y' => Game::Draw,
            'Z' => Game::Win,
            _ => panic!("Invalid char"),
        };
        for c in ['X', 'Y', 'Z'] {
            if play(fst, c) == res {
                sum += play(fst, c).points() + get_points(c)
            }
        }
    }
    sum
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_solve_first() {
        assert_eq!(15, solve_first(SAMPLE));
        assert_eq!(11666, solve_first(INPUT))
    }

    #[test]
    fn test_solve_second() {
        assert_eq!(12, solve_second(SAMPLE));
        assert_eq!(12767, solve_second(INPUT))
    }

    #[test]
    fn test_play() {
        assert_eq!(Game::Win, play('A', 'Y'));
        assert_eq!(Game::Draw, play('A', 'X'));
        assert_eq!(Game::Lose, play('C', 'Y'));
    }
}

